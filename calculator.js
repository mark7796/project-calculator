const add = function(a, b) {
    return a + b;
};

const substract = function(a, b) {
    return a - b;
};

const multiply = function(a, b) {
    return a * b;
};

const divide = function(a, b) {
    return a / b;
};

console.log(add(7, 3));
console.log(substract(7, 3));
console.log(multiply(7, 3));
console.log(divide(7, 3));